#include "gpio_ctrl.h"
#include <stdint.h>
 /****************************************************************
 * Constants
 ****************************************************************/

#define MAX_BUF 64

/****************************************************************
 * gpio_export
 ****************************************************************/
int gpio_wait_for_int(int fd, int ms)
{
    struct pollfd polls;
    uint8_t c;
    int x;
    lseek(fd, 0, SEEK_SET);
    (void)read(fd, &c, 1);
    //printf("start gpio value %d\n",c);
    polls.fd = fd;
    polls.events = POLLPRI | POLLERR;
    x = poll(&polls, 1, ms);
    if(x > 0)
    {
        lseek(fd, 0, SEEK_SET);
        (void)read(fd, &c, 1);
    //printf("end gpio value %d\n",c);
    }
    return x;
}
int gpio_export(unsigned int gpio)
{
	int fd, len;
	char buf[MAX_BUF];

	fd = open("/sys/class/gpio/export", O_WRONLY);
	if (fd < 0) {
		perror("gpio/export");
		return fd;
	}

	len = snprintf(buf, sizeof(buf), "%d", gpio);
	write(fd, buf, len);
	close(fd);

	return 0;
}

